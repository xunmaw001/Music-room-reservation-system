<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
												leqibianhao: '',
																leqimingcheng: '',
																fenlei: '',
																tupian: '',
																mingshiyanzou: '',
																leshimingcheng: '',
																guimo: '',
																leshiweizhi: '',
																leqixiangqing: '',
												},
																												fenleiOptions: [],
				fenleiIndex: 0,
																																leshimingchengOptions: [],
				leshimingchengIndex: 0,
																																				// 登陆用户信息
				user: {},
			}
		},
		components: {
			wPicker
		},
		computed: {
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
						
					},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
						// ss读取
																																																												
																								// 下拉框
			res = await this.$api.option(`leqifenlei`,`fenlei`,{});
			this.fenleiOptions = res.data;
																								// 下2
			res = await this.$api.option(`leshixinxi`,`leshimingcheng`,{});
			this.leshimingchengOptions = res.data;
																											
			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`leqi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
										if(o=='leqibianhao'){
					this.ruleForm.leqibianhao = obj[o];
					continue;
					}
										if(o=='leqimingcheng'){
					this.ruleForm.leqimingcheng = obj[o];
					continue;
					}
										if(o=='fenlei'){
					this.ruleForm.fenlei = obj[o];
					continue;
					}
										if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					continue;
					}
										if(o=='mingshiyanzou'){
					this.ruleForm.mingshiyanzou = obj[o];
					continue;
					}
										if(o=='leshimingcheng'){
					this.ruleForm.leshimingcheng = obj[o];
					continue;
					}
										if(o=='guimo'){
					this.ruleForm.guimo = obj[o];
					continue;
					}
										if(o=='leshiweizhi'){
					this.ruleForm.leshiweizhi = obj[o];
					continue;
					}
										if(o=='leqixiangqing'){
					this.ruleForm.leqixiangqing = obj[o];
					continue;
					}
									}
																																																																															}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																										// 下二随
			async leshimingchengChange (e) {
				this.leshimingchengIndex = e.target.value
				this.ruleForm.leshimingcheng = this.leshimingchengOptions[this.leshimingchengIndex]
				let res = await this.$api.follow(`leshixinxi`, `leshimingcheng`,{
					columnValue: this.ruleForm.leshimingcheng
				});
																																																												if(res.data.guimo){
					this.ruleForm.guimo = res.data.guimo
				}
																if(res.data.leshiweizhi){
					this.ruleForm.leshiweizhi = res.data.leshiweizhi
				}
																			},
																								
			// 多级联动参数
																																																												
																																																									
																																																									
																					// 下拉变化
			fenleiChange(e) {
				this.fenleiIndex = e.target.value
				this.ruleForm.fenlei = this.fenleiOptions[this.fenleiIndex]
			},
																																										
																																				tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
															mingshiyanzouTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.mingshiyanzou = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
																																													
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																																																																if((!this.ruleForm.leqibianhao)){
					this.$utils.msg(`乐器编号不能为空`);
					return
				}
																																								if((!this.ruleForm.leqimingcheng)){
					this.$utils.msg(`乐器名称不能为空`);
					return
				}
																																								if((!this.ruleForm.fenlei)){
					this.$utils.msg(`分类不能为空`);
					return
				}
																																																																																																																																																																																																																																				if(this.ruleForm.id){
					await this.$api.update(`leqi`, this.ruleForm);
				}else{
					await this.$api.add(`leqi`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>


				this.ruleForm.refid = options.refid;
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`leshixinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
										if(o=='leshibianhao'){
					this.ruleForm.leshibianhao = obj[o];
					continue;
					}
										if(o=='leshimingcheng'){
					this.ruleForm.leshimingcheng = obj[o];
